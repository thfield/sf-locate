const under10 = [
  '1ST',
  '2ND',
  '3RD',
  '4TH',
  '5TH',
  '6TH',
  '7TH',
  '8TH',
  '9TH',
]

/** @function standardize
 * @param {string} address
 * @returns {object} standardized address object with at minimum properties: ['number', 'street', 'type', 'zipcode']
 */
function standardize (address) {
  if (typeof address !== 'string') { throw new Error('input to method addressParse.standardize must be a string') }
  address = address.replace(/![a-zA-Z0-9]/g, '').toUpperCase()
  let addressObj = parser.parseLocation(address)

  if (!addressObj.street || !addressObj.number ) {
    throw new Error('input to method addressParse.standardize must be an address. Missing property street or number.')
  }
  // change spelled out numbered streets to digits
  let ordinalStreet = wordsToNumbers(addressObj.street)
  if (typeof ordinalStreet === 'number') {
    addressObj.street = ordinal(ordinalStreet).toUpperCase()
  }
  // zeropad numbered streets under 10
  if (under10.includes(addressObj.street)) {
    addressObj.street = `0${addressObj.street}`
  }

  // abbreviate street type
  if (addressObj.type) { addressObj.type = Street_Type[addressObj.type] }

  // // change "zip" to "zipcode"
  // addressObj.zipcode = addressObj.zip || addressObj.zipcode
  // delete addressObj.zip

  return addressObj
}

/** @function nextDoor
 * @param {object} address - a normalized address object
 * @param {string} upDown - find next street number going up or down
 * @returns {object} - adds or subtracts 2 to the street number
 */
function nextDoor (address, upDown = 'up') {
  let res = Object.assign({}, address)
  res.number = (upDown === 'down') ? +res.number - 2 : +res.number + 2
  res.number = res.number.toString()

  return res
}

